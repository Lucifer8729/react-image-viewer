const style = {
  position: "absolute",
  top: "50%",
  left: "50%",
  transform: "translate(-50%, -50%)",
  maxWidth: "50vw",
  outline: 0,
};

const Images = () => {
  const [open, setOpen] = React.useState({ status: false, image: "" });
  const handleOpen = (image) => {
    setOpen({ status: true, image: image });
  };
  const handleClose = () => setOpen({ status: false, image: "" });

  return (
    <>
      <Box
        sx={{
          width: "100%",
          height: "100%",
          overflowY: "auto",
          mt: 8,
          textAlign: "center",
        }}
      >
        <ImageList variant="masonry" cols={8} gap={8}>
          {itemData.map((item, i) => (
            <ImageListItem
              key={i}
              onClick={() => handleOpen(item.img)}
              sx={{ cursor: "pointer" }}
            >
              <img
                src={`${item.img}?w=248&fit=crop&auto=format`}
                srcSet={`${item.img}?w=248&fit=crop&auto=format&dpr=2 2x`}
                alt={item.title}
                loading="lazy"
              />
            </ImageListItem>
          ))}
        </ImageList>
        <IconButton
          sx={{
            bgcolor: "#2196f3",
            "&: hover": {
              bgcolor: "#4dabf5",
            },
          }}
        >
          <ExpandMoreIcon sx={{ color: "white" }} />
        </IconButton>
      </Box>
      <Modal
        open={open.status}
        onClose={handleClose}
        sx={{ overflowY: "auto" }}
      >
        <Box sx={style}>
          <IconButton
            sx={{
              right: 0,
              bottom: 0,
              transform: "translate(-50%, -50%)",
              position: "absolute",
              bgcolor: "#2196f3",
              "&: hover": {
                bgcolor: "#4dabf5",
              },
              boxShadow: 24,
            }}
            onClick={() => {
              downloadImage(open.image, false);
            }}
          >
            <FileDownloadIcon sx={{ color: "white" }} />
          </IconButton>
          <img
            style={{ width: "100%" }}
            src={`${open.image}`}
            alt="open-img"
            loading="lazy"
          />
        </Box>
      </Modal>
    </>
  );
};

export default Images;
